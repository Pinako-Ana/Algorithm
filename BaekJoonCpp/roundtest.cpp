#include <iostream>
#include <cmath>
using namespace std;

int main(void){

	double a = -123.1;
	double b = -10.7;
	cout << "round" << endl;

	cout << round(a) << endl;
	cout << round(b) <<endl;
	
	cout << "ceil" << endl;

	cout << ceil(a) << endl;
	cout << ceil(b) << endl;
	
	cout << "floor" << endl;

	cout << floor(a) <<endl;
	cout << floor(b) << endl;


	return 0;
}